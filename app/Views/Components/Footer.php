        const currentPath = window.location.pathname;
        const menuItems = document.querySelectorAll('.onepage-menu li');

        let bestMatch = null;
        let longestMatchLength = -1;

        menuItems.forEach(li => {
            const link = li.querySelector('a');
            const href = link.getAttribute('href');
            const linkPath = new URL(href, window.location.origin).pathname;

        // Check if it's a prefix of the current path
        if (currentPath.startsWith(linkPath) && linkPath.length > longestMatchLength) {
            bestMatch = li;
            longestMatchLength = linkPath.length;
        }
    });

        // Clear existing active classes
        menuItems.forEach(li => li.classList.remove('active'));

        // Set the most specific match as active
        if (bestMatch) {
            bestMatch.classList.add('active');
        }
